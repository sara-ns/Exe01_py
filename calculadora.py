print('PROGRAMA CALCULADORA')

valor1 = float(input('Digite o primeiro valor'))
valor2 = float(input('Digite o segundo valor'))

soma = valor1 + valor2
print("A soma entre os dois valores é {}".format(soma))

subtracao = valor1 - valor2
print('A subtração entre os dois valores é {}'.format(subtracao))

multiplicacao = valor1 * valor2
print('A multiplicação entre os dois valores é {}'.format(multiplicacao))

divisao = valor1 / valor2
print('A divisão entre os dois numeros é {}'.format(divisao))

